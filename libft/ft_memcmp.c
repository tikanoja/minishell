#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t				i;
	const unsigned char	*s1c;
	const unsigned char	*s2c;

	i = 0;
	s1c = (const unsigned char *)s1;
	s2c = (const unsigned char *)s2;
	while (i < n)
	{
		if (s1c[i] != s2c[i])
			return (s1c[i] - s2c[i]);
		i++;
	}
	return (0);
}
