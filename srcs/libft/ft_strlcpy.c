#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	srclen;

	i = 0;
	srclen = (unsigned int)ft_strlen((char *)src);
	if (dstsize <= 0)
		return (srclen);
	if (srclen == 0)
	{
		dst[i] = '\0';
		return (0);
	}
	while (i < dstsize - 1 && src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (srclen);
}
